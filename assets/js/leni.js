var node = document.getElementsByTagName("body")[0];
node.innerHTML = '';
for (i = 0; i < 360; i = i + 10){
  for (s = 0; s < 100; s = s + 5) {
    var rainbow = document.createElement("div");
    
    rainbow.classList.add("blockLeni");
    rainbow.style = "background-color:hsl(" + i + ","+s+"%,50%)";
    node.appendChild(rainbow);
  }
}